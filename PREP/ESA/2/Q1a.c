/*

To get the output of C code using gcc (GNU Compiler Collection), several options can be used:

Basic Compilation:
gcc file.c
./a.out
Compiles file.c and generates an executable named a.out by default.

Specify Output File:
gcc file.c -o output (or output.exe)
./output
Compiles file.c and names the output executable output (or output.exe).

Enable All Warnings:
gcc -Wall file.c
./a.out
Enables all common warning messages to help identify potential issues in the code.

For debugging (GDB)
gcc -g file.c -o prog
./prog

*/